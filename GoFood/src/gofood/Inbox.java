    private int showTabel(JTable Tabel, String status){
        DefaultTableModel tbl = new DefaultTableModel(){
                                public Class getColumnClass(int c) {
                                  switch (c) {
                                    case 0: return Boolean.class;
                                    default: return String.class;
                                  }   
                                } };
            tbl.addColumn("pilih");
            tbl.addColumn("id");
            tbl.addColumn("Dari");
            tbl.addColumn("Pesan");
            Tabel.setModel(tbl);
        try{
            Statement statement = (Statement) conek.GetConnection().createStatement();
            ResultSet res = statement.executeQuery("SELECT * FROM pesan WHERE id_user='"+user+"' AND baca="+status);
            while (res.next()){
                tbl.addRow(new Object []{
                    false,
                    res.getString("id"),
                    res.getString("dari"),
                    res.getString("pesan")
                });
                Tabel.setModel(tbl);
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "Tabel Error!!!");
        }
        Tabel.getColumnModel().getColumn(0).setMaxWidth(40);
        Tabel.getColumnModel().getColumn(2).setMaxWidth(80);
        TableColumnModel tcm = Tabel.getColumnModel();
        tcm.removeColumn(tcm.getColumn(1));
        Tabel.setShowHorizontalLines(true);
        Tabel.setDefaultEditor(Object.class, null);
        return tbl.getRowCount();
    }

