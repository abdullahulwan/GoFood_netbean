    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        TableModel model = tbl_unread.getModel();
        ArrayList<String> row = new ArrayList<>();
        for (int i = 0; i < row1; i++) {
            if(Boolean.valueOf(model.getValueAt(i, 0).toString())){
                row.add(model.getValueAt(i, 1).toString());
            }
        }
        row.forEach((id) -> {
            objek.editRow("pesan", "baca=0", "id='"+id+"'", null, null);
        });
        row1 = showTabel(tbl_unread,"1");
        row2 = showTabel(tbl_readed,"0");
        pil_semua.setSelected(false);
    }//GEN-LAST:event_jButton1ActionPerformed

    private void tbl_unreadMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbl_unreadMouseClicked
        if(evt.getClickCount() == 2){
            int index = tbl_unread.getSelectedRow();
            TableModel model = tbl_unread.getModel();
            String id = model.getValueAt(index, 1).toString();
            String from = model.getValueAt(index, 2).toString();
            String pesan = model.getValueAt(index, 3).toString();
            String Message = "Dari : "+from+"\n\n"+pesan+"";
            JOptionPane.showMessageDialog(null, Message, "Info Pesanan", JOptionPane.PLAIN_MESSAGE);
            objek.editRow("pesan", "baca=0", "id='"+id+"'", null, null);
            row1 = showTabel(tbl_unread,"1");
            row2 = showTabel(tbl_readed,"0");
        }
    }//GEN-LAST:event_tbl_unreadMouseClicked

