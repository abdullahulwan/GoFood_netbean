    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        TableModel model = setting.getModel();
        ArrayList<String> row = new ArrayList<>();
        int n = 0;
        for (int i = 0; i < model.getRowCount(); i++) {
            if(Boolean.valueOf(model.getValueAt(i, 0).toString())){
                row.add(model.getValueAt(i, 1).toString());
                n++;
            }
        }
        String img;
        for (int i=0; i< row.size(); i++) {
            img = objek.getString("menu", "id_menu='"+row.get(i)+"'", "gambar_menu").substring(5);
            objek.deldata(img);
            objek.delRow("menu","id_menu='"+row.get(i)+"'", null, null);
        }
        pil_semua.setSelected(false);
        showtable();
    }//GEN-LAST:event_jButton3ActionPerformed

