    private void showtable(){
        DefaultTableModel tbl = new DefaultTableModel(){
                                public Class getColumnClass(int c) {
                                  switch (c) {
                                    case 0: return Boolean.class;
                                    default: return String.class;
                                  }   
                                } };
        tbl.addColumn("Pilih");
        tbl.addColumn("id_menu");
        tbl.addColumn("Publikasi");
        tbl.addColumn("Menu");
        tbl.addColumn("Sumber Gambar");
        tbl.addColumn("Harga");
        tbl.addColumn("Diskon");
        tbl.addColumn("Deskripsi");
        setting.setModel(tbl);
        setting.getColumnModel().getColumn(0).setMaxWidth(40);
        setting.getColumnModel().getColumn(2).setMaxWidth(70);
        setting.getColumnModel().getColumn(3).setPreferredWidth(80);
        setting.getColumnModel().getColumn(4).setMaxWidth(200);
        setting.getColumnModel().getColumn(4).setMinWidth(150);
        setting.getColumnModel().getColumn(5).setMaxWidth(70);
        setting.getColumnModel().getColumn(6).setMaxWidth(70);
        setting.getColumnModel().getColumn(7).setPreferredWidth(200);
        TableColumnModel tcm = setting.getColumnModel();
        tcm.removeColumn(tcm.getColumn(1));
        setting.setShowHorizontalLines(true);
        String publish;
        int lenght = id_resto.length()+17;
        try{
            Statement st = (Statement) conek.GetConnection().createStatement();
            ResultSet rs = st.executeQuery("SELECT * FROM menu WHERE id_restoran='"+id_resto+"'");
            while(rs.next()){
                if (rs.getBoolean("publish")){
                    publish = "YA";
                }else{
                    publish = "TIDAK";
                }
                tbl.addRow(new Object []{
                    false,
                    rs.getString("id_menu"),
                    publish,
                    rs.getString("menu"),
                    rs.getString("gambar_menu").substring(lenght),
                    objek.formatUang(rs.getString("harga_menu")),
                    objek.formatUang(rs.getString("diskon_menu")),
                    rs.getString("deskripsi_menu")
                });
                setting.setModel(tbl);
            }
            rs.close();
        }catch(SQLException e){
            e.getStackTrace();
            JOptionPane.showMessageDialog(null, "Pencarian gagal");
        }
        setting.setDefaultEditor(Object.class, null);
    }
